async function updateList(type: "whitelist" | "blacklist", user: GuildMember, newData: string[]) {
  try {
    await prisma.voiceChannel.update({
      where: {
        id: user.id
      },
      data: {
        [type]: "[" + newData.join(", ") + "]"
      }
    });
  } catch (error) {
    console.error(error);
  }
}

